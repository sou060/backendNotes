function saveAllData() {
  const data = getStoredData();

  // QA ratings
  data.qaRatings = {};
  document.querySelectorAll(".qa-card").forEach((card) => {
    if (card.dataset.rating) {
      data.qaRatings[card.dataset.qaId] = card.dataset.rating;
    }
  });

  // STAR stories
  data.starStories = [];
  document.querySelectorAll(".star-card").forEach((card) => {
    const title = card.querySelector(".story-title")?.value || "";
    const texts = Array.from(card.querySelectorAll("textarea")).map(
      (t) => t.value,
    );
    data.starStories.push({ title, texts });
  });

  // Mistakes
  data.mistakes = [];
  document.querySelectorAll(".mistake-row").forEach((row) => {
    const body = row.querySelector(".m-body");
    if (body) {
      data.mistakes.push({
        html: body.innerHTML,
        severity: row.dataset.severity,
      });
    }
  });

  // Reviewed sections are managed by the button click directly
  // to prevent un-loaded chapters from losing their state!
  setStoredData(data);
}

function loadSavedData() {
  const data = getStoredData();

  // QA ratings
  if (data.qaRatings) {
    document.querySelectorAll(".qa-card").forEach((card) => {
      const rating = data.qaRatings[card.dataset.qaId];
      if (rating) {
        card.dataset.rating = rating;
        const rateBtn = card.querySelector(`.rate-btn[data-rate="${rating}"]`);
        if (rateBtn) rateBtn.classList.add("active");
      }
    });
  }

  // STAR stories
  if (data.starStories && data.starStories.length > 0) {
    // Clear default seeds first
    const starList = document.getElementById("star-list");
    if (starList) starList.innerHTML = "";
    starCount = 0;
    data.starStories.forEach((story) => {
      addStarStory(story.title);
      const card = document.getElementById("star-" + starCount);
      if (card) {
        const textareas = card.querySelectorAll("textarea");
        story.texts.forEach((t, i) => {
          if (textareas[i]) textareas[i].value = t;
        });
      }
    });
  }

  // Mistakes
  if (data.mistakes && data.mistakes.length > 0) {
    const mistakeList = document.getElementById("mistake-list");
    if (mistakeList) mistakeList.innerHTML = "";
    data.mistakes.forEach((m) => {
      mistakeCount++;
      const id = "mistake-" + mistakeCount;
      const row = document.createElement("div");
      row.className = "mistake-row";
      row.id = id;
      row.dataset.severity = m.severity;
      const color = m.severity === "weak" ? "#C0392B" : "#C98A1D";
      row.innerHTML = `
            <span class="m-severity" style="background:${color}"></span>
            ${m.html}
            <button class="m-del" onclick="document.getElementById('${id}').remove(); updateMistakeEmptyState(); saveAllData();">✕</button>`;
      mistakeList.prepend(row);
    });
  }

  // Reviewed sections
  if (data.reviewedSections) {
    data.reviewedSections.forEach((secId) => {
      const btn = document.querySelector(
        `.section-review-btn[data-section="${secId}"]`,
      );
      if (btn) btn.classList.add("reviewed");
    });
  }

  updateProgressBar();
}

