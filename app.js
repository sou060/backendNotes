window.currentChapterIndex = 1;
document.addEventListener("keydown", function (e) {
  // Don't capture when typing in inputs
  if (
    e.target.tagName === "INPUT" ||
    e.target.tagName === "TEXTAREA" ||
    e.target.isContentEditable
  ) {
    if (e.key === "Escape") {
      e.target.blur();
      return;
    }
    return;
  }

  const kbdOverlay = document.getElementById("kbd-overlay");

  switch (e.key) {
    case "/":
    case "k":
    case "K":
      if (
        e.key === "/" ||
        (e.key.toLowerCase() === "k" && (e.ctrlKey || e.metaKey))
      ) {
        e.preventDefault();
        const si = document.getElementById("search-input");
        if (si) si.focus();
      }
      break;
    case "Escape":
      // Close keyboard modal
      if (kbdOverlay && kbdOverlay.classList.contains("open")) {
        kbdOverlay.classList.remove("open");
        break;
      }
      // Close search results
      const sr = document.getElementById("search-results");
      if (sr) {
        sr.classList.remove("show");
        sr.innerHTML = "";
      }
      // Close mobile sidebar
      closeMobileSidebar();
      break;
    case "ArrowLeft":
      if (document.querySelector("#view-notes:not([hidden])")) {
        e.preventDefault();
        window.currentChapterIndex = Math.max(
          1,
          window.currentChapterIndex - 1,
        );
        goToChapter(window.currentChapterIndex);
      }
      break;
    case "ArrowRight":
      if (document.querySelector("#view-notes:not([hidden])")) {
        e.preventDefault();
        const maxCh2 = document.querySelectorAll(".chapter").length;
        window.currentChapterIndex = Math.min(
          maxCh2,
          window.currentChapterIndex + 1,
        );
        goToChapter(window.currentChapterIndex);
      }
      break;
    case "d":
    case "D":
      toggleTheme();
      break;
    case "?":
      toggleKbdModal();
      break;
  }
});

