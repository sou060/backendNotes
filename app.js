// ---- Flashcard Mode Logic ----
let currentFlashcardIndex = 0;
let flashcardCards = [];

function toggleFlashcardMode(isActive) {
  const qaList = document.getElementById("qa-list");
  const controls = document.getElementById("flashcard-controls");

  if (isActive) {
    qaList.classList.add("flashcard-mode-active");
    controls.style.display = "flex";
    flashcardCards = Array.from(document.querySelectorAll(".qa-card")).filter(
      (c) => c.style.display !== "none",
    );
    currentFlashcardIndex = 0;
    updateFlashcardView();
  } else {
    qaList.classList.remove("flashcard-mode-active");
    controls.style.display = "none";
    const activeFilter = document.querySelector(
      "#qa-filters .filter-chip.active",
    );
    if (activeFilter) {
      activeFilter.click();
    } else {
      document
        .querySelectorAll(".qa-card")
        .forEach((c) => (c.style.display = ""));
    }
  }
}

function updateFlashcardView() {
  if (flashcardCards.length === 0) return;
  flashcardCards.forEach((c, idx) => {
    c.style.display = idx === currentFlashcardIndex ? "" : "none";
    c.classList.remove("open");
  });
  const counter = document.getElementById("flashcard-counter");
  if (counter) {
    counter.textContent = `${currentFlashcardIndex + 1} / ${flashcardCards.length}`;
  }
  const currentCardId = flashcardCards[currentFlashcardIndex].id;
  const reviewBtn = document.getElementById("flashcard-review-btn");
  if (reviewBtn) {
    if (flashcardProgress[currentCardId] === "review") {
      reviewBtn.style.opacity = "0.7";
      reviewBtn.textContent = "Unmark Review";
    } else {
      reviewBtn.style.opacity = "1";
      reviewBtn.textContent = "⚠️ Mark for Review";
    }
  }
}

function prevFlashcard() {
  if (currentFlashcardIndex > 0) {
    currentFlashcardIndex--;
    updateFlashcardView();
  }
}

function nextFlashcard() {
  if (currentFlashcardIndex < flashcardCards.length - 1) {
    currentFlashcardIndex++;
    updateFlashcardView();
  }
}

function toggleReviewCurrent() {
  if (flashcardCards.length === 0) return;
  const currentCard = flashcardCards[currentFlashcardIndex];
  if (!currentCard.id) {
    currentCard.id = `qa-${currentCard.dataset.chapter}-${currentFlashcardIndex}`;
  }
  if (flashcardProgress[currentCard.id] === "review") {
    delete flashcardProgress[currentCard.id];
  } else {
    flashcardProgress[currentCard.id] = "review";
  }
  localStorage.setItem(
    "backend-notes-flashcard-progress",
    JSON.stringify(flashcardProgress),
  );
  updateFlashcardView();
}

document.addEventListener("keydown", (e) => {
  const toggle = document.getElementById("flashcard-toggle");
  if (toggle && toggle.checked) {
    if (e.key === "ArrowLeft") {
      prevFlashcard();
    } else if (e.key === "ArrowRight") {
      nextFlashcard();
    } else if (e.key === " " || e.key === "Enter") {
      if (flashcardCards.length > 0) {
        const currentBtn =
          flashcardCards[currentFlashcardIndex].querySelector(".qa-q");
        if (currentBtn) {
          e.preventDefault();
          toggleQA(currentBtn);
        }
      }
    }
  }
});

