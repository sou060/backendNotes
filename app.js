function buildCramSheet() {
  const out = document.getElementById("cram-output");
  let html = "";

  // Weak / shaky Q&A
  const flagged = Array.from(document.querySelectorAll(".qa-card")).filter(
    (c) => c.dataset.rating === "weak" || c.dataset.rating === "shaky",
  );
  html +=
    '<div class="cram-section"><h3>Questions you flagged as shaky or weak</h3>';
  if (flagged.length === 0) {
    html +=
      '<p class="empty-note">Nothing flagged yet. Go to Interview Q&amp;A and rate a few cards, then regenerate.</p>';
  } else {
    flagged
      .sort(
        (a, b) =>
          (a.dataset.rating === "weak" ? 0 : 1) -
          (b.dataset.rating === "weak" ? 0 : 1),
      )
      .forEach((card) => {
        const q = card.querySelector(".qa-q__text")?.textContent || "";
        const a = card.querySelector(".qa-a")?.textContent || "";
        const tagText = card.dataset.rating === "weak" ? "WEAK" : "SHAKY";
        html += `<div class="cram-item"><span class="cram-tag ${card.dataset.rating}">${tagText}</span><div class="cram-q">${q}</div><div>${a}</div></div>`;
      });
  }
  html += "</div>";

  // STAR stories
  html += '<div class="cram-section"><h3>Your STAR stories</h3>';
  const stories = Array.from(document.querySelectorAll(".star-card"));
  const filledStories = stories.filter((s) =>
    Array.from(s.querySelectorAll("textarea")).some((t) => t.value.trim()),
  );
  if (filledStories.length === 0) {
    html += '<p class="empty-note">No STAR stories filled in yet.</p>';
  } else {
    filledStories.forEach((s) => {
      const title = s.querySelector(".story-title")?.value || "Untitled story";
      const [sit, task, act, res] = Array.from(
        s.querySelectorAll("textarea"),
      ).map((t) => t.value.trim());
      html += `<div class="cram-item"><div class="cram-q">${title}</div>
        ${sit ? "<p><b>S:</b> " + sit + "</p>" : ""}
        ${task ? "<p><b>T:</b> " + task + "</p>" : ""}
        ${act ? "<p><b>A:</b> " + act + "</p>" : ""}
        ${res ? "<p><b>R:</b> " + res + "</p>" : ""}</div>`;
    });
  }
  html += "</div>";

  // Mistakes log
  html += '<div class="cram-section"><h3>Your mistakes log</h3>';
  const mistakes = Array.from(document.querySelectorAll(".mistake-row"));
  if (mistakes.length === 0) {
    html += '<p class="empty-note">No mistakes logged yet.</p>';
  } else {
    mistakes.forEach((m) => {
      html += `<div class="cram-item">${m.querySelector(".m-body").innerHTML}</div>`;
    });
  }
  html += "</div>";

  out.innerHTML = html;
  out.scrollIntoView({ behavior: "smooth", block: "start" });
}

