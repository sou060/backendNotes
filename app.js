function showView(id) {
  document.querySelectorAll(".view").forEach((v) => (v.hidden = v.id !== id));
  document
    .querySelectorAll(".nav-pin")
    .forEach((p) => p.classList.toggle("active", p.dataset.view === id));
  document.getElementById("crumb-text").textContent =
    document.querySelector("#" + id + " [data-crumb]")?.dataset.crumb || id;

  // Render Mermaid diagrams if switching to diagrams view
  if (id === "view-diagrams" && typeof mermaid !== "undefined") {
    setTimeout(() => {
      const unrendered = document.querySelectorAll(
        '#view-diagrams .mermaid:not([data-processed="true"])',
      );
      if (unrendered.length > 0) {
        mermaid.init(undefined, unrendered);
      }
    }, 10);
  }

  window.scrollTo({ top: 0 });
  closeMobileSidebar();
}

window.loadChapter = async function (ci) {
  const container = document.getElementById("notes-container");
  if (!container) return;

  // Check if already loaded
  if (container.querySelector(`.chapter[data-chapter="${ci}"]`)) return;

  container.innerHTML =
    '<div style="padding:40px;text-align:center;">Loading chapter...</div>';

  try {
    const res = await fetch(`./notes/chapter-${ci}.html`);
    if (!res.ok) throw new Error("Not found");
    const html = await res.text();
    container.innerHTML = html;

    if (window.hljs) {
      container.querySelectorAll("pre code").forEach((block) => {
        hljs.highlightElement(block);
      });
    }

    enhanceCodeCards(container);

    // Inject review buttons
    injectReviewButtons();

    // Restore saved reviewed states for this new chapter
    const data = getStoredData();
    if (data.reviewedSections) {
      data.reviewedSections.forEach((secId) => {
        const btn = container.querySelector(
          `.section-review-btn[data-section="${secId}"]`,
        );
        if (btn) btn.classList.add("reviewed");
      });
    }

    // Run mermaid if present
    if (typeof mermaid !== "undefined") {
      setTimeout(() => {
        mermaid.init(undefined, container.querySelectorAll(".mermaid"));
      }, 10);
    }

    // Update progress
    updateProgressBar();

    // Observe for scroll spy
    const newChapter = container.querySelector(
      `.chapter[data-chapter="${ci}"]`,
    );
    if (newChapter && window.chapterObserver) {
      window.chapterObserver.observe(newChapter);
    }

    // Save last viewed chapter
    localStorage.setItem("backend-notes-last-chapter", ci);
  } catch (e) {
    container.innerHTML = `<div style="padding:40px;color:red;">Error loading chapter ${ci}</div>`;
  }
};

